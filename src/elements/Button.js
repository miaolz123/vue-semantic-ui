export const Button = {
  template: '<div :class="className"><slot>Default</slot></div>',
  props: {
    class: {
      type: String,
      default: '',
    },
    css: {
      type: String,
      default: '',
    },
  },
  computed: {
    className: function () {
      return this.class ? this.class : this.css ? `ui ${this.css} button` : "ui button"
    },
  },
}

